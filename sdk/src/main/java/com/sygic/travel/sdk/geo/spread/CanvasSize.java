package com.sygic.travel.sdk.geo.spread;

/**
 * Dimensions of a canvas.
 */
public class CanvasSize {
	public int width;
	public int height;

	/**
	 * Map canvas size.
	 * @param width
	 * @param height
	 */
	public CanvasSize(int width, int height) {
		this.width = width;
		this.height = height;
	}
}
